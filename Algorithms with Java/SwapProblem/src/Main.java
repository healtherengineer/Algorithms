
public class Main {

    public static void main(String[] args) {

        int num1 = 20;
        int num2 = 10;

        System.out.println("Value of num1 : " + num1 + "\nValue of num2 : " + num2);
        System.out.println("----------------------------");
        int temp;
        // Swap
        temp = num1;
        num1 = num2;
        num2 = temp;
        System.out.println("Value of num1 : " + num1 + "\nValue of num2 : " + num2);

    }

}
